<h4>Error al cargar el módulo de generacion (Gen Lib).</h4>
<p>Posibles Razones del error:</p>
<p>Es posible que el módulo haya sido eliminado del sistema, (desinstalado).</p>
<p>Es posible que el módulo instalado no es valido archivo incorrecto.</p>
<p>Es posible que el módulo este corrupto, codigo (sun) invalido.</p>
<p><b>¿Como solucionar este problema?</b></p>
<p>Intente configurar el bus nuevamente,  si el problema persiste siga los siguientes pasos, solo para el super admin.</p>
<p>Desisntale este módulo del sistema.</p>
<p>Vuleva a instalar un módulo valido que cumpla con las normas de Express Bus.</p>
<p>Si ya instalo  , recuerde también modificar el mdulo del bus, esto en el gestor de buses (editar).</p>
<p>Mas información sobre las módulos (Lib Gen) en la web del <a href="http://www.arielmax.com.ar/express_bus/" target="_blank">autor</a>.</p>
